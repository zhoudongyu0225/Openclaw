#-------------------------------------------------------------------------------
# 生成汇报
#-------------------------------------------------------------------------------
generate_report() {
    local report_file="$REPORT_DIR/daily-detail-${DATE}.md"
    generate_daily_tasks > "$report_file"
    echo "已生成: $report_file"
}

#-------------------------------------------------------------------------------
# 发送汇报
#-------------------------------------------------------------------------------
send_report() {
    local report_file="$REPORT_DIR/daily-detail-${DATE}.md"
    
    # 读取报告内容
    local report_content=$(cat "$report_file")
    
    # 发送到飞书
    openclaw message send \
        --channel feishu \
        --target oc_a63b77f46415a883feef0b106c5519d1 \
        --message "$report_content" 2>&1
    
    echo "汇报已发送"
}

#-------------------------------------------------------------------------------
# 主逻辑
#-------------------------------------------------------------------------------
case "$1" in
    generate)
        generate_report
        ;;
    send)
        send_report
        ;;
    show)
        cat "$REPORT_DIR/daily-detail-${DATE}.md" 2>/dev/null || generate_daily_tasks
        ;;
    *)
        generate_report
        send_report
        ;;
esac
